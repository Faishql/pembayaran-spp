package com.example.pembayaran_spp;

class ComboItem
{
    private String key;
    private String value;

    public ComboItem()
    {
        this.key = key;
        this.value = value;
    }

    public ComboItem(String kelas, String xii) {
    }

    @Override
    public String toString()
    {
        return key;
    }

    public String getKey()
    {
        return key;
    }

    public String getValue()
    {
        return value;
    }
}
